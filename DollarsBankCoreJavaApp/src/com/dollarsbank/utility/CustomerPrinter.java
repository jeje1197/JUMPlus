package com.dollarsbank.utility;

import com.dollarsbank.model.Customer;

public class CustomerPrinter {
	public static void print(Customer customer) {
		ConsolePrinter.print(ConsolePrinter.ANSI_BLUE, PrettyFormatter.format("Customer Information:"));
		ConsolePrinter.print("Name: " + customer.getName());
		ConsolePrinter.print("Address: " + customer.getAddress());
		ConsolePrinter.print("Phone Number: " + customer.getPhoneNumber() + "\n");
	}
}
